package Aula05;

public class PraticaAula {

	public static void main(String[] args) {
		
		//Resolução do exercício
		for(int i=2;i<=40;i+=2) {
			System.out.println(i+" ");
		}
		
		//Varie a variável de controle de 1 a 100 em incrementos de 1
		for(int i=1;i<=100;i++) {
		}
		//Varie a variável de controle de 100 a 1 em decrementos de 1
		for(int i=100;i>=1;i--) {
		}
		//Varie a variável de controle de 7 a 77 em incrementos de 7
		for(int i=7;i<=77;i=i+7) {
		}
		//Varie a variável de controle de 20 a 2 em decrementos de 2
		for(int i=20;i>=2;i-=2) {
		}
		//Varie a variável de controle em relação aos valores 2,5,8,11,14,17,20
		for(int i=2;i<=20;i+=3) {
		}
		//Varie a variável de controle em relação aos valores 99,88,77,66,55,44,33,22,11,0.
		for(int i=99;i>=0;i-=11) {
		}
	}

}
